# Introduction of the whole program
print('''Hello, This is a program in which you can get both square and cube
of any number entered by you!

TRY IT!!''') 

# giving input method
a = input("Enter a Number: ") 

# converting variable 'a' from class string to class integer
a = int(a)

# giving calculation method for the value entered by the user
square = a*a
cube = a*a*a


# printing the results out
print("The square of",a, "is", square)
print("The cube of", a, "is", cube)
print("THANKS FOR USING!!")

